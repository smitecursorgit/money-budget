export async function parseFinanceText(
  text: string,
  categories: Array<{ name: string; type: string; keywords: string[] }>,
  today: string
): Promise<ParsedEntry[]> {
  const categoryList = categories.map((c) => `${c.name} (${c.type}): ${c.keywords.join(', ')}`).join('\n');

  const userPrompt = `Сегодня: ${today}
Категории пользователя (ПРИОРИТЕТ — используй их названия если подходят):
${categoryList || 'Нет пользовательских категорий — используй стандартные из инструкции.'}

Команда для разбора: "${text}"`;

  const response = await getGroqClient().chat.completions.create({
    model: 'llama-3.3-70b-versatile',
    messages: [
      { role: 'system', content: SYSTEM_PROMPT },
      { role: 'user', content: userPrompt },
    ],
    temperature: 0.05,
    max_tokens: 400,
  });

  const raw = response.choices[0]?.message?.content?.trim() || '{}';
  // Strip markdown code fences if model wraps output despite instructions
  const content = raw.replace(/^```(?:json)?\n?/i, '').replace(/\n?```$/i, '').trim();

  try {
    const raw = JSON.parse(content);
    const normalized = Array.isArray(raw) ? raw : [raw];
    const validated = ParsedEntriesSchema.parse(normalized) as ParsedEntry[];
    // Постобработка: сокращаем заметки вида "Потратил X на Y" → "Y"
    validated.forEach((e) => {
      if (e.note) e.note = shortenNote(e.note);
    });
    return validated;
  } catch {
    throw new Error('AI вернул некорректный ответ. Попробуйте сформулировать иначе.');
  }
}
