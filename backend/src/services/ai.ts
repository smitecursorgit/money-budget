import OpenAI from 'openai';
import fs from 'fs';
import { z } from 'zod';
import { ParsedEntry } from '../types/index';

const ParsedEntrySchema = z.object({
  type: z.enum(['expense', 'income', 'reminder']),
  amount: z.number().positive().nullable().optional(),
  category: z.string().max(100).nullable().optional(),
  date: z.string().nullable().optional(),
  note: z.string().max(500).nullable().optional(),
  reminderTitle: z.string().max(200).nullable().optional(),
  reminderRecurrence: z.enum(['once', 'daily', 'weekly', 'monthly', 'yearly']).nullable().optional(),
});

const ParsedEntriesSchema = z.array(ParsedEntrySchema).min(1).max(10);

/** Извлекает краткую суть заметки: "Потратил 100 рублей на шоколадку" → "шоколадка" */
function shortenNote(note: string | null | undefined): string | null | undefined {
  if (!note || typeof note !== 'string') return note;
  let t = note.trim();
  // "Потратил X на Y" → Y
  const m1 = t.match(/^потратил\s+.+?\s+на\s+(.+)$/i);
  if (m1) t = m1[1].trim();
  else if (/^купил\s+/i.test(t)) t = t.replace(/^купил\s+/i, '').trim();
  else if (/^получил\s+/i.test(t)) t = t.replace(/^получил\s+/i, '').trim();
  else if (/^заработал\s+/i.test(t)) {
    const m = t.match(/на\s+(.+)$/i);
    t = m ? m[1].trim() : 'подработка';
  }
  // Убираем суффиксы "за 500", "500 р", "100 рублей" в конце
  t = t.replace(/\s+за\s+\d+[\sрруб.]*$/i, '').replace(/\s+\d+[\sрруб.]*$/i, '').trim();
  return t || note;
}

// Groq uses OpenAI-compatible API — only baseURL and models differ.
// Lazy-initialized to avoid throwing at module load time when GROQ_API_KEY is missing.
let _groq: OpenAI | null = null;
function getGroqClient(): OpenAI {
  if (!_groq) {
    const apiKey = process.env.GROQ_API_KEY;
    if (!apiKey) {
      throw new Error('GROQ_API_KEY environment variable is not set');
    }
    _groq = new OpenAI({ apiKey, baseURL: 'https://api.groq.com/openai/v1' });
  }
  return _groq;
}

export async function transcribeAudio(filePath: string): Promise<string> {
  const transcription = await getGroqClient().audio.transcriptions.create({
    file: fs.createReadStream(filePath),
    model: 'whisper-large-v3',
    language: 'ru',
  });
  return transcription.text;
}

